export const SidebarMotion = ({ children }) => {
  const [openState, setOpenState] = useState(false);
  const open = openState;
  const setOpen = setOpenState;

  return (
    <motion.div
      className="h-full px-4 py-4 hidden md:flex md:flex-col bg-transparent w-[300px] flex-shrink-0"
      animate={{
        width: open ? "300px" : "110px",
      }}
      onMouseEnter={() => setOpen(true)}
      onMouseLeave={() => setOpen(false)}
    >
      {children}
    </motion.div>
  );
};

